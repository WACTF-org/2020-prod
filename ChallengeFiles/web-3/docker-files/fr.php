<h1>Manifeste pour le développement Agile de logiciels
</h1>
<br><br><br>

<p>

<font size="+2">
Nous découvrons comment mieux développer des logiciels<br>
par la pratique et en aidant les autres à le faire.<br>
Ces expériences nous ont amenés à valoriser :<br>

</font>

<p>
<font size="+2"></font><font size="+3">Les individus et leurs interactions </font><font size="+2">plus que les processus et les outils<br></font>
<font size="+2"></font><font size="+3">Des logiciels opérationnels </font><font size="+2">plus qu’une documentation exhaustive<br></font>
<font size="+2"></font><font size="+3">La collaboration avec les clients  </font><font size="+2">plus que la négociation contractuelle<br></font>
<font size="+2"></font><font size="+3">L’adaptation au changement </font><font size="+2">plus que le suivi d’un plan<br></font>


<p>
<font size="+2">
Nous reconnaissons la valeur des seconds éléments,<br>
mais privilégions les premiers.<br>

</font>
