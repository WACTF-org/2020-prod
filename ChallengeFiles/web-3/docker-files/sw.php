<h1>Manifesto ya Utengenezaji wa Programu kutumia njia ya Agile
</h1>
<br><br><br>

<p>

<font size="+2">
Tunazindua njia bora za utengenezaji wa programu<br>
Kwa kutengeneza programu na kusaidia wengine kufanya hivyo<br>
Kupitia kazi hii tumeweza kuthamini haya maadili:<br>

</font>

<p>
<font size="+2"></font><font size="+3">Watu binafsi na mwingiliiano </font><font size="+2">juu ya taratibu na zana<br></font>
<font size="+2"></font><font size="+3">Programu inayofanya kazi </font><font size="+2">juu ya nyaraka kina<br></font>
<font size="+2"></font><font size="+3">Wateja kushirikiana </font><font size="+2">juu ya mazungumzo kuhusu mikataba<br></font>
<font size="+2"></font><font size="+3">Kujukumia mabadiliko </font><font size="+2">juu ya kufwata mpangilio<br></font>


<p>
<font size="+2">
Yaani, wakati kuna thamani katika vitu vilivyo upande <br>
Wa kulia, tunathamini vilivyo upande wa kushoto zaidi.<br>

</font>
