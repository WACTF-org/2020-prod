<h1>Manifiesto por el Desarrollo Ágil de Software
</h1>
<br><br><br>

<p>

<font size="+2">
Estamos descubriendo formas mejores de desarrollar<br>
software tanto por nuestra propia experiencia como<br>
ayudando a terceros. A través de este trabajo hemos<br>
aprendido a valorar:<br>

</font>

<p>
<font size="+2"></font><font size="+3">Individuos e interacciones </font><font size="+2">sobre procesos y herramientas<br></font>
<font size="+2"></font><font size="+3">Software funcionando </font><font size="+2">sobre documentación extensiva<br></font>
<font size="+2"></font><font size="+3">Colaboración con el cliente </font><font size="+2">sobre negociación contractual<br></font>
<font size="+2"></font><font size="+3">Respuesta ante el cambio </font><font size="+2">sobre seguir un plan<br></font>


<p>
<font size="+2">
Esto es, aunque valoramos los elementos de la derecha,<br>
valoramos más los de la izquierda.<br>

